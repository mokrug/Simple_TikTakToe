        }

        private void Window_SizeChanged(object sender, SizeChangedEventArgs e)
        {
            if (e.WidthChanged)
            {
                this.Width = e.NewSize.Height;
            }
            else
            {
                this.Height = e.NewSize.Width;
            }
        }
